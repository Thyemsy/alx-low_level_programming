/**
 * _islower - check if charater c is in lowercase and return the 1 else  return 0
 * Return: Always return the value of 0
 */
int _islower(int c)
{
	c = _islower('c');
	_putchar(c + '0');
	c = _islower('o');
	_putchar(c + 'C');
	c = _islower(108);
	_putchar(c + '0');
	_putchar('\n');
	return (0);
}
